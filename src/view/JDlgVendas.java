    public void beanView(Vendas vendas){
                
         jTxtCodigo.setText(String.valueOf(vendas.getIdVenda())); 
         SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
         jFmtDataVenda.setText(sdf.format(vendas.getDataVenda()));
         jCboCliente.setSelectedIndex(vendas.getIdCliente()); 
         jCboFuncionario.setSelectedIndex(vendas.getIdFuncionario()); 
         jTxtPagamento.setText(vendas.getFormaPagamento());
         jTxtTotal.setText(String.valueOf(vendas.getValorTotal())); 
         jTxtObservacoes.setText(String.valueOf(vendas.getObservacoes())); 
         
    }

