    private void jBtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAlterarActionPerformed
        // TODO add your handling code here:
        jTxtCodigo.setEnabled(false);
        if (jTxtCodigo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pesquise antes de alterar");
            return;
        }
        habilitar(true);
        incluir = false;
        jTxtCodigo.setEnabled(false);
        jFmtDataVenda.grabFocus();
    }//GEN-LAST:event_jBtnAlterarActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
        Vendas vendas = new Vendas();

        int cod = Integer.parseInt(jTxtCodigo.getText());
        vendas.setIdVenda(cod);

        int vt = Integer.parseInt(jTxtTotal.getText());
        vendas.setValorTotal(vt);
        
        vendas.setFormaPagamento(jTxtPagamento.getText());
        vendas.setObservacoes(jTxtObservacoes.getText());
        
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        try {
            Date dataVend = formato.parse(jFmtDataVenda.getText());
            vendas.setDataVenda(dataVend);
        } catch (ParseException ex) {
            Logger.getLogger(JDlgProduto.class.getName()).log(Level.SEVERE, null, ex);
        }

        VendasDao vendasDao = new VendasDao();
        if (incluir == true) {
            vendasDao.insert(vendas);
        } else {
            vendasDao.update(vendas);
        }

        habilitar(false);
        limpar();
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

