    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
         jTxtCodigo.setEnabled(false);
        if (jTxtCodigo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pesquise antes de excluir");
            return;
        } else {
        int resp = JOptionPane.showConfirmDialog(null, "Deseja excluir?");
        if (resp == JOptionPane.YES_OPTION) {
            Vendas vendas = new Vendas();
            int cod = Integer.parseInt(jTxtCodigo.getText());
        vendas.setIdVenda(cod);
        int cod2 = Integer.parseInt(jTxtCodigo.getText());
        vendas.setIdCliente(cod2);
        int cod3 = Integer.parseInt(jTxtCodigo.getText());
        vendas.setIdFuncionario(cod3);
        int cod4 = Integer.parseInt(jTxtCodigo.getText());
        vendas.setValorTotal(cod4);
        
        vendas.setFormaPagamento(jTxtPagamento.getText());
        vendas.setObservacoes(jTxtObservacoes.getText());
        
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        try {
            Date dataVend = formato.parse(jFmtDataVenda.getText());
            vendas.setDataVenda(dataVend);
        } catch (ParseException ex) {
            Logger.getLogger(JDlgProduto.class.getName()).log(Level.SEVERE, null, ex);
        }

            VendasDao vendasDao = new VendasDao();
            vendasDao.delete(vendas);
            limpar();
        }
        }
        limpar();

    }//GEN-LAST:event_jBtnExcluirActionPerformed

